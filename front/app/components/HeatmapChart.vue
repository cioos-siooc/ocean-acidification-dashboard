function initChart() {
    if (!chartContainer.value) return;
    try { if (chart) { chart.dispose(); chart = null; } } catch (_) {}
    registerEchartsDarkTheme();
    chart = echarts.init(chartContainer.value, 'dark', { renderer: 'canvas' });
    chart.setOption(buildOption());
    chart.resize();
}

function updateChart() {
    if (!chart) return;
    chart.setOption(buildOption(), { notMerge: true });
}

// ── Resize observer ───────────────────────────────────────────────────────────

let resizeObserver: ResizeObserver | null = null;

onMounted(() => {
    initChart();
    if (chartContainer.value) {
        resizeObserver = new ResizeObserver(() => chart?.resize());
        resizeObserver.observe(chartContainer.value);
    }
});

onBeforeUnmount(() => {
    resizeObserver?.disconnect();
    try { chart?.dispose(); } catch (_) {}
    chart = null;
});

// ── Reactivity ────────────────────────────────────────────────────────────────

watch(() => [props.times, props.depths, props.data, props.min, props.max], updateChart, { deep: true });

// ── Expose for parent use ─────────────────────────────────────────────────────

defineExpose({ loading });
